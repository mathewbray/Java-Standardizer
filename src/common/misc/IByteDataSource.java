// PACKAGE


package common.misc;

//----------------------------------------------------------------------


// IMPORTS


import common.exception.AppException;

//----------------------------------------------------------------------


// BYTE DATA SOURCE INTERFACE


public interface IByteDataSource
	extends IDataInput
{

////////////////////////////////////////////////////////////////////////
//  Member classes : non-inner classes
////////////////////////////////////////////////////////////////////////


	// BYTE DATA CLASS


	public static class ByteData
	{

	////////////////////////////////////////////////////////////////////
	//  Constructors
	////////////////////////////////////////////////////////////////////

		public ByteData(byte[] data)
		{
			this.data = data;
			length = data.length;
		}

		//--------------------------------------------------------------

		public ByteData(byte[] data,
						int    offset,
						int    length)
		{
			this.data = data;
			this.offset = offset;
			this.length = length;
		}

		//--------------------------------------------------------------

	////////////////////////////////////////////////////////////////////
	//  Instance fields
	////////////////////////////////////////////////////////////////////

		public	byte[]	data;
		public	int		offset;
		public	int		length;

	}

	//==================================================================

////////////////////////////////////////////////////////////////////////
//  Methods
////////////////////////////////////////////////////////////////////////

	ByteData getData()
		throws AppException;

	//------------------------------------------------------------------

}

