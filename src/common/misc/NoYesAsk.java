package common.misc;

//----------------------------------------------------------------------


// NO-YES-ASK OPTION ENUMERATION


public enum NoYesAsk
	implements IStringKeyed
{

////////////////////////////////////////////////////////////////////////
//  Constants
////////////////////////////////////////////////////////////////////////

	NO
	(
		"no", "false"
	),

	YES
	(
		"yes", "true"
	),

	ASK
	(
		"ask"
	);

////////////////////////////////////////////////////////////////////////
//  Constructors
////////////////////////////////////////////////////////////////////////

	private NoYesAsk(String... keys)
	{
		this.keys = keys;
	}

	//------------------------------------------------------------------

////////////////////////////////////////////////////////////////////////
//  Class methods
////////////////////////////////////////////////////////////////////////

	public static NoYesAsk forKey(String key)
	{
		for (NoYesAsk value : values())
		{
			for (String k : value.keys)
			{
				if (k.equals(key))
					return value;
			}
		}
		return null;
	}

	//------------------------------------------------------------------

////////////////////////////////////////////////////////////////////////
//  Instance methods : IStringKeyed interface
////////////////////////////////////////////////////////////////////////

	public String getKey()
	{
		return keys[0];
	}

	//------------------------------------------------------------------

////////////////////////////////////////////////////////////////////////
//  Instance methods : overriding methods
////////////////////////////////////////////////////////////////////////

	@Override
	public String toString()
	{
		return StringUtils.firstCharToUpperCase(getKey());
	}

	//------------------------------------------------------------------

////////////////////////////////////////////////////////////////////////
//  Instance fields
////////////////////////////////////////////////////////////////////////

	private	String[]	keys;

}

