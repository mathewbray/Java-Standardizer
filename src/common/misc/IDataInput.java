package common.misc;

//----------------------------------------------------------------------


// DATA INPUT INTERFACE


public interface IDataInput
{

////////////////////////////////////////////////////////////////////////
//  Enumerated types
////////////////////////////////////////////////////////////////////////


	// DATA INPUT KIND


	public enum Kind
	{

	////////////////////////////////////////////////////////////////////
	//  Constants
	////////////////////////////////////////////////////////////////////

		BYTE_STREAM,
		BYTE_SOURCE,
		DOUBLE_STREAM,
		DOUBLE_SOURCE;

	////////////////////////////////////////////////////////////////////
	//  Instance methods
	////////////////////////////////////////////////////////////////////

		public boolean isByteInput()
		{
			return ((this == BYTE_STREAM) || (this == BYTE_SOURCE));
		}

		//--------------------------------------------------------------

		public boolean isDoubleInput()
		{
			return ((this == DOUBLE_STREAM) || (this == DOUBLE_SOURCE));
		}

		//--------------------------------------------------------------

	}

	//==================================================================

////////////////////////////////////////////////////////////////////////
//  Methods
////////////////////////////////////////////////////////////////////////

	long getLength();

	//------------------------------------------------------------------

	void reset();

	//------------------------------------------------------------------

}

